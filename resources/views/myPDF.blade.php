<h6 style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    <strong><u>(SELANJUTNYA DALAM SURAT PERJANJIAN INI DISEBUT PIHAK KEDUA)</u></strong>
</h6>
<br>
<p style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    Dengan ini menyatakan bahwa kedua belah pihak telah sepakat mengadakan Perjanjian Kontrak untuk kegiatan
    penyuburan Lahan dan Peningkatan Produksi Padi, seperti diuraikan dalam pasal-pasal di bawah ini :
</p>
<br>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    <strong>Pasal 1</strong>
</p>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    RUANG LINGKUP PEKERJAAN
</p>
<br>
<p style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    PIKAH PERTAMA dalam kedudukannya tersebut di atas telah sepakat mengikat perjanjian dengan PIHAK KEDUA
    dalam usaha peningkatan kesuburan lahan dan peningkatan produksi padi.
</p>
<div style="page-break-after: always"></div>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    <strong>Pasal 2</strong>
</p>
<p style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    Maksud perjanjian kerja sama ini untuk memenuhi kebutuhan dan/atau memperoleh manfaat bagi
    <strong>PARA PIHAK</strong> dalam peningkatan kesuburan lahan dan peningkatan produktivitas padi.
</p>
<br>

<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    <strong>Pasal 3</strong>
</p>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    JANGKA WAKTU PERJANJIAN
</p>
<p style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    Jangka waktu perjanjian ini berlangsung 1 (satu) kali panen atau 4 (empat) bulan mulai dari persiapan tanam
    sampai dengan panen. Dan perjanjian dapat diperpanjang kembali apabila telah terjadi kesepakatan kedua belah pihak.
</p>
<br>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    <strong>Pasal 4</strong>
</p>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    <strong>HAK DAN KEWAJIBAN PARA PIHAK</strong>
</p>
<p style="text-align: center; margin-top: 10px; margin-bottom: 5px;">
    <strong>KEWAJIBAN PARA PIHAK</strong>
</p>
<p style="text-align: left; margin-top: 5px; margin-bottom: 5px;">
    <strong>(1) PIHAK PERTAMA</strong>
</p>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    a. Menyediakan pinjaman berupa saprodi kepada pihak KEDUA
</p>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    b. Mengirim dan mendistribusikan saprodi kepada pihak KEDUA
</p>
<p style="text-align: left; margin-top: 5px; margin-bottom: 5px;">
    <strong>(2) PIHAK KEDUA</strong>
</p>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    a. Membayar pinjaman saprodi kepada pihak PERTAMA dalam bentuk uang tunai atau dengan cara membayar dengan
    menjual gabah sesuai harga pasar kepada pihak PERTAMA senilai pinjaman yang diberikan pihak PERTAMA; Besaran
    Pinjaman
    Pihak KEDUA sebagaimana terlampir
</p>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    b. Menerapkan paket teknologi.
</p>
<br>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    <strong>HAK PARA PIHAK</strong>
</p>
<p style="text-align: left; margin-top: 5px; margin-bottom: 5px;">
    <strong>(1) PIHAK PERTAMA</strong>
</p>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    a. Menerima pembayaran pinjaman Saprodi dari pihak KEDUA dalam bentuk uang tunai atau dengan cara menerima penjualan
    gabah
    dari pihak KEDUA senilai pinjaman yang diberikan pihak PERTAMA; Besaran Pinjaman Pihak KEDUA sebagaimana terlampir;
</p>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    b. Mengawasi dan mengontrol penerapan paket teknologi.
</p>
<p style="text-align: left; margin-top: 15px; margin-bottom: 5px;">
    <strong>(2) PIHAK KEDUA</strong>
</p>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    a. Menerima pinjaman berupa Saprodi dari Pihak PERTAMA.
</p>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    b. Menerima pengiriman dan pendistribusian saprodi dari Pihak Pertama.
</p>
<div style="page-break-after: always"></div>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    PENYELESAIAN PERSELISIHAN
</p>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    <strong>Pasal 5</strong>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    (1) Apabila terjadi Perselisihan dalam Perjanjian Kerjasama ini, PARA PIHAK sepakat akan
    menyelesaikan secara musyawarah guna mencapai mufakat;
</p>
<p class="hang" style="text-align: justify; margin-top: 5px; margin-bottom: 5px;">
    (2) Apabila upaya penyelesaian sebagaimana dimaksud pada ayat 1 (satu) tidak tercapai,
    PARA PIHAK sepakat untuk menyelesaikan melalui Pengadilan Negeri Situbondo
</p>
<br>
<p style="text-align: center; margin-top: 5px; margin-bottom: 5px;">
    PENUTUP
</p>
